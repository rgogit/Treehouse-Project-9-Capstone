// change element colors on hover

// header
document.getElementById("head").addEventListener("mouseover", function() {
    document.getElementById("head").style.backgroundColor = "#62bbbb";
});    
document.getElementById("head").addEventListener("mouseout", function() {
    document.getElementById("head").style.backgroundColor = "#008080";
});

